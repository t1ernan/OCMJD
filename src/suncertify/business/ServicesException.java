package suncertify.business;

public class ServicesException extends Exception {

	/**
	 * Instantiates a new services exception.
	 */
	public ServicesException() {
		super();
	}

	/**
	 * Instantiates a new services exception.
	 *
	 * @param message
	 *            the message
	 */
	public ServicesException(String message) {
		super(message);
	}

}
